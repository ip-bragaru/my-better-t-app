import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");

const sourcePath = path.join(projectRoot, "tokens", "design-tokens.json");
const themeCssPath = path.join(projectRoot, "styles", "theme.css");
const designTokensTsPath = path.join(
  projectRoot,
  "src",
  "shared",
  "config",
  "design-tokens.generated.ts",
);
const fontsTsPath = path.join(projectRoot, "src", "shared", "config", "fonts.ts");

const source = JSON.parse(fs.readFileSync(sourcePath, "utf8"));

const themeCss = [
  "/* This file is auto-generated by scripts/generate-design-tokens.mjs */",
  "@theme {",
  `  --font-sans: "${source.fontFamily.body.medium}";`,
  "",
  "  /* App colors */",
  ...toCssCustomProperties("color-app", source.color),
  "",
  "  /* App radii */",
  ...toCssCustomProperties("radius-app", source.radius, { unit: "px" }),
  "}",
  "",
  "@utility font-medium {",
  `  font-family: "${source.fontFamily.body.medium}";`,
  "  font-weight: 500;",
  "}",
  "",
  "@utility font-semibold {",
  `  font-family: "${source.fontFamily.body.semiBold}";`,
  "  font-weight: 600;",
  "}",
  "",
  "@utility font-bold {",
  `  font-family: "${source.fontFamily.body.bold}";`,
  "  font-weight: 700;",
  "}",
  "",
].join("\n");

const designTokensTs = [
  "// This file is auto-generated by scripts/generate-design-tokens.mjs",
  `export const DESIGN_TOKENS = ${toTsObjectLiteral(source)} as const;`,
  "",
].join("\n");

const fontsTs = [
  "// This file is auto-generated by scripts/generate-design-tokens.mjs",
  `export const FONT_FAMILIES = ${toTsObjectLiteral(source.fontFamily.body)} as const;`,
  "",
].join("\n");

fs.writeFileSync(themeCssPath, themeCss);
fs.writeFileSync(designTokensTsPath, designTokensTs);
fs.writeFileSync(fontsTsPath, fontsTs);

function toKebabCase(value) {
  return value.replace(/[A-Z]/g, (match) => `-${match.toLowerCase()}`);
}

function toCssCustomProperties(prefix, value, options = {}) {
  const unit = options.unit ?? "";

  return flattenObject(value).map(([key, nestedValue]) => {
    const cssValue =
      typeof nestedValue === "number" && unit ? `${nestedValue}${unit}` : nestedValue;

    return `  --${prefix}-${key}: ${cssValue};`;
  });
}

function flattenObject(value, parentKey = "") {
  if (!value || typeof value !== "object" || Array.isArray(value)) {
    return [];
  }

  return Object.entries(value).flatMap(([key, nestedValue]) => {
    const nextKey = parentKey ? `${parentKey}-${toKebabCase(key)}` : toKebabCase(key);

    if (
      nestedValue &&
      typeof nestedValue === "object" &&
      !Array.isArray(nestedValue)
    ) {
      return flattenObject(nestedValue, nextKey);
    }

    return [[nextKey, nestedValue]];
  });
}

function toTsObjectLiteral(value, indent = 0) {
  const pad = " ".repeat(indent);
  const innerPad = " ".repeat(indent + 2);

  if (typeof value === "string") {
    return JSON.stringify(value);
  }

  if (typeof value === "number" || typeof value === "boolean") {
    return String(value);
  }

  if (Array.isArray(value)) {
    return `[${value.map((item) => toTsObjectLiteral(item, indent)).join(", ")}]`;
  }

  if (value && typeof value === "object") {
    const entries = Object.entries(value).map(
      ([key, nestedValue]) => `${innerPad}${key}: ${toTsObjectLiteral(nestedValue, indent + 2)}`,
    );

    return `{\n${entries.join(",\n")}\n${pad}}`;
  }

  return "undefined";
}
